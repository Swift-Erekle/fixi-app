export function normalizeCategoryText(value) {
  return String(value || '')
    .trim()
    .replace(/^[^A-Za-z0-9ა-ჰ]+/g, '')
    .replace(/\s+/g, ' ')
    .toLowerCase();
}

function cleanCategoryText(value) {
  return String(value || '')
    .trim()
    .replace(/^[^A-Za-z0-9ა-ჰ]+/g, '')
    .replace(/\s+/g, ' ');
}

function uniqueValues(values) {
  const seen = new Set();
  const out = [];
  values.forEach((value) => {
    const clean = cleanCategoryText(value);
    const key = normalizeCategoryText(clean);
    if (!clean || seen.has(key)) return;
    seen.add(key);
    out.push(clean);
  });
  return out;
}

function getReplacementList(value) {
  const key = normalizeCategoryText(value);
  const found = VALUE_REPLACEMENTS.find(([from]) => normalizeCategoryText(from) === key);
  if (!found) return null;
  return Array.isArray(found[1]) ? found[1] : [found[1]];
}

function getCategoryTerms(category) {
  return [category.name, ...(category.subs || []), ...(CATEGORY_ALIASES[category.id] || [])];
}

export function getCategoryByValue(value) {
  const clean = cleanCategoryText(value);
  if (!clean) return null;
  const keys = [clean, ...(getReplacementList(clean) || [])].map(normalizeCategoryText);
  return CATEGORIES.find((category) =>
    getCategoryTerms(category).some((term) => keys.includes(normalizeCategoryText(term)))
  ) || null;
}

export function normalizeCategoryValue(value) {
  const clean = cleanCategoryText(value);
  if (!clean) return null;
  const replacement = getReplacementList(clean);
  if (replacement?.length) return cleanCategoryText(replacement[0]);
  const category = getCategoryByValue(clean);
  if (!category) return clean;
  const exactSub = (category.subs || []).find((sub) => normalizeCategoryText(sub) === normalizeCategoryText(clean));
  return exactSub || category.name;
}

export function normalizeCategoryList(values) {
  const raw = Array.isArray(values) ? values : typeof values === 'string' ? values.split(',') : [];
  return uniqueValues(raw.flatMap((value) => getReplacementList(value) || [normalizeCategoryValue(value)]));
}

export function normalizeMainCategory(value) {
  return getCategoryByValue(value)?.name || normalizeCategoryValue(value);
}

export function normalizeMainCategoryList(values) {
  const raw = Array.isArray(values) ? values : typeof values === 'string' ? values.split(',') : [];
  return uniqueValues(raw.flatMap((value) => {
    const replacements = getReplacementList(value);
    return (replacements?.length ? replacements : [value]).map(normalizeMainCategory).filter(Boolean);
  }));
}

export function getCategorySearchTerms(categoryName) {
  const category = getCategoryByValue(categoryName);
  if (!category) return categoryName ? [categoryName] : [];
  return [category.name, ...(category.subs || []), ...(CATEGORY_ALIASES[category.id] || [])];
}

export function categorySearchMatches(values, query, translate = null) {
  const q = normalizeCategoryText(query);
  if (!q) return true;
  return (Array.isArray(values) ? values : [values]).some((value) => {
    const raw = normalizeCategoryText(value);
    const translated = typeof translate === 'function' ? normalizeCategoryText(translate(value)) : '';
    return raw.includes(q) || translated.includes(q);
  });
}

export function filterCategoriesBySearch(query, translate = null) {
  if (!normalizeCategoryText(query)) return CATEGORIES;
  return CATEGORIES.filter((category) =>
    categorySearchMatches(getCategoryTerms(category), query, translate)
  );
}

export function filterSubcategoriesBySearch(subs, query, translate = null) {
  const list = Array.isArray(subs) ? subs : [];
  if (!normalizeCategoryText(query)) return list;
  return list.filter((sub) => categorySearchMatches(sub, query, translate));
}

function getHandymanCategoryValues(handyman) {
  const values = [
    handyman?.specialty,
    ...(Array.isArray(handyman?.specialties) ? handyman.specialties : []),
    ...(Array.isArray(handyman?.services) ? handyman.services : []),
  ].filter(Boolean);

  return uniqueValues(values.flatMap((value) => [
    value,
    normalizeCategoryValue(value),
    normalizeMainCategory(value),
    ...normalizeCategoryList([value]),
  ]));
}

export function handymanMatchesCategory(handyman, categoryName) {
  const needles = new Set(getCategorySearchTerms(categoryName).map(normalizeCategoryText));
  if (!needles.size) return true;
  return getHandymanCategoryValues(handyman).some((value) => needles.has(normalizeCategoryText(value)));
}

